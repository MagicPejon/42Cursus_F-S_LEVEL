#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	res;

	i = 0;
	sign = 1;
	res = 0;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (str[i + 1] != '\0')
			res += (str[i] - '0') * 10;
		if (str[i + 1] == '\0')
			res += (str[i] - '0');
	}
	return (res * sign);
}
